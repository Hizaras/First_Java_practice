public class Calculation {

	public int addNumber(int num1, int num2) {
	return  num1 + num2;
	}

	public int substractNumber(int num1, int num2) {
	return num1 - num2;
	}

	public int divideNumber(int num1, int num2) {
	return num1 / num2;
	}
	
	public int multiplyNumber(int num1, int num2) {
	return num1 * num2;
	}
	



}